-- ####### Quick File Access #######
-- open obsidian vaultâ€™s todo.md with <leader>ot
vim.keymap.set("n", "<leader>t", function()
  vim.cmd.edit(vim.fn.expand("~/.config/nvim/path/to/your/vault/todo.md"))
end, { desc = "Open Obsidian Vault's todo.md" })

